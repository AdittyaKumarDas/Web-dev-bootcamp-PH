function difference(num1, num2) {
  const diff = num1 - num2;
  return diff;
}

const firstNumber = 40; // ⁡⁢⁣⁣Arguments⁡
const secondNumber = 50; // ⁡⁢⁣⁣Arguments⁡
const result = difference(firstNumber, secondNumber);

if (result > 0) {
  console.log("Positive Number");
} else {
  console.log("Negative Number");
}
