// A roler coster example

const age = 12;
const height = 70;

if (age >= 12 && height >= 120) {
  console.log("You can ride a roller coster");
} else {
  console.log("You can't");
}

// Ternary Operator

age >= 12 && height >= 80
  ? console.log("You can ride a roller coster")
  : console.log("You can't");
